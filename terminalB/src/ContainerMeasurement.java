public class ContainerMeasurement { // programmer-defined class to store container measurements

    double arrivalTime; // time at which container arrives inside terminal
    double startUnloadTime; // time at which container is unloaded
    double endUnloadTime; // time at which unloading is finished
    double startStorageTime; // time at which container is stored
    double endStorageTime; // time at which container is removed from storage
    double startLoadTime; // time at which container is processed for loading onto trucks
    double endLoadTime; // time at which container loading onto trucks is finished
    public int shipIdentifier; // ship number

}
